"""One-time host/cluster setup that has to be in place before a session runs.

Three pieces:
  * Local container registry (a long-lived Deployment + Service) that the host
    pushes built images to via `docker push localhost:5000/...` and that
    containerd pulls from on the same node.
  * k3s /etc/rancher/k3s/registries.yaml allowing insecure (HTTP) pulls from
    localhost:5000. We do NOT touch this file with sudo from agent-uplink;
    when it's missing, we print the exact shell commands and exit so the user
    runs them once.
  * mitmproxy CA cert + key on the host (~/.agent_uplink/mitm/), generated by
    a one-shot pod against the mitm image with a hostPath mount.
"""

from __future__ import annotations

import logging
import os
import shutil
import time
from pathlib import Path

from .k8s import (
    apply_manifests,
    deployment_manifest,
    hostpath_volume,
    kubectl,
    namespace_exists,
    namespace_manifest,
    pod_manifest,
    service_manifest,
    wait_for_deployment_ready,
    wait_for_pod_succeeded,
)
from .process import run_command

LOGGER = logging.getLogger("agent-uplink")

REGISTRY_NAMESPACE = "agent-uplink-system"
REGISTRY_DEPLOYMENT = "registry"
REGISTRY_HOST_PORT = 5000
REGISTRY_HOSTPATH = "/var/lib/agent-uplink-registry"
REGISTRY_IMAGE_DEFAULT = "registry:2"
REGISTRY_PUSH_ENDPOINT = "localhost:5000"

K3S_REGISTRIES_PATH = Path("/etc/rancher/k3s/registries.yaml")


# ---------------------------------------------------------------------------
# registries.yaml check
# ---------------------------------------------------------------------------


_REGISTRIES_YAML_SETUP = """mirrors:
  "localhost:5000":
    endpoint:
      - "http://localhost:5000"
configs:
  "localhost:5000":
    tls:
      insecure_skip_verify: true
"""

_REGISTRIES_YAML_INSTRUCTIONS = f"""
One-time k3s setup is required so containerd can pull from the local registry.
Run these commands, then re-run agent-uplink:

  sudo mkdir -p /etc/rancher/k3s
  sudo tee {K3S_REGISTRIES_PATH} > /dev/null <<'EOF'
{_REGISTRIES_YAML_SETUP}EOF
  sudo systemctl restart k3s
"""


def check_registries_yaml() -> None:
    """Verify k3s knows about the local insecure registry. Exit with a
    helpful error if not."""
    if not K3S_REGISTRIES_PATH.exists():
        raise SystemExit(
            f"{K3S_REGISTRIES_PATH} not found.\n" + _REGISTRIES_YAML_INSTRUCTIONS
        )
    content = K3S_REGISTRIES_PATH.read_text(encoding="utf-8")
    if "localhost:5000" not in content:
        raise SystemExit(
            f"{K3S_REGISTRIES_PATH} doesn't mention localhost:5000.\n"
            + _REGISTRIES_YAML_INSTRUCTIONS
        )


# ---------------------------------------------------------------------------
# Local registry deployment
# ---------------------------------------------------------------------------


def _registry_manifests(image: str) -> list[dict]:
    labels = {"app": "registry"}
    pod_spec: dict = {
        "hostNetwork": True,
        "dnsPolicy": "ClusterFirstWithHostNet",
        "containers": [
            {
                "name": "registry",
                "image": image,
                "imagePullPolicy": "IfNotPresent",
                "ports": [
                    {
                        "containerPort": 5000,
                        "hostPort": REGISTRY_HOST_PORT,
                        "protocol": "TCP",
                    }
                ],
                "env": [
                    {"name": "REGISTRY_HTTP_ADDR", "value": "0.0.0.0:5000"},
                    {
                        "name": "REGISTRY_STORAGE_DELETE_ENABLED",
                        "value": "true",
                    },
                ],
                "volumeMounts": [{"name": "data", "mountPath": "/var/lib/registry"}],
                "resources": {"limits": {"memory": "256Mi", "cpu": "1"}},
            }
        ],
        "volumes": [
            hostpath_volume(
                "data", REGISTRY_HOSTPATH, hp_type="DirectoryOrCreate"
            )
        ],
    }
    return [
        namespace_manifest(REGISTRY_NAMESPACE),
        deployment_manifest(
            REGISTRY_DEPLOYMENT,
            REGISTRY_NAMESPACE,
            labels=labels,
            pod_spec=pod_spec,
        ),
        service_manifest(
            REGISTRY_DEPLOYMENT,
            REGISTRY_NAMESPACE,
            selector=labels,
            port=REGISTRY_HOST_PORT,
            target_port=5000,
        ),
    ]


def ensure_registry(image: str = REGISTRY_IMAGE_DEFAULT) -> None:
    """Apply the registry deployment (idempotent) and wait for readiness."""
    LOGGER.info("ensuring local registry deployment")
    apply_manifests(_registry_manifests(image))
    wait_for_deployment_ready(REGISTRY_NAMESPACE, REGISTRY_DEPLOYMENT, timeout=120)


